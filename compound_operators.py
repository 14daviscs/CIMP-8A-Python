#!/usr/bin/env python3

# Incrementing
counter = 0
print("Incrementing starting point: " + str(counter))
counter = counter + 1
print("counter = " + str(counter))
counter += 1
print("counter = " + str(counter))
print()

# Decrementing
counter = 10
print("Decrementing starting point: " + str(counter))
counter = counter - 1
print("counter = " + str(counter))
counter -= 1
print("counter = " + str(counter))
